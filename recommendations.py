# recommendations.py

def get_recommendations(analysis_result):
    level = analysis_result["level"]

    if level == "Критический":
        return [
            "Немедленно обратитесь за профессиональной помощью.",
            "Позвоните на горячую линию поддержки или обратитесь в кризисный центр.",
            "Попробуйте поговорить с близкими."
        ]
    elif level == "Высокий":
        return [
            "Рекомендуется записаться на онлайн-консультацию с психологом.",
            "Посетите групповые тренинги по управлению стрессом.",
            "Изучите материалы по самооценке и прокрастинации."
        ]
    elif level == "Средний":
        return [
            "Рассмотрите возможность участия в тренингах по коммуникации и уверенности.",
            "Пользуйтесь онлайн-материалами и приложениями для самопомощи.",
            "Уделяйте больше внимания сну, питанию и физической активности."
        ]
    else:
        return [
            "Продолжайте следить за своим психоэмоциональным состоянием.",
            "Используйте профилактические статьи и видео по саморазвитию.",
            "Делитесь своими чувствами с друзьями или наставником."
        ]
